# numbers in lists are cardinal rather than ordinal
# order of numbers is not important
# lists start at index 0 - so subtract 1 from ordinal list

animals = ['bear', 'python3.6', 'peacock', 'kangaroo', 'whale', 'platypus']

print(f"The animal at 1 is the second animal and is a {animals[1]}.")
print(f"The third animal is the animal at index 2, and is {animals[2]}.")
print(f"The first animal is at index 0; oh my, it's a {animals[0]}!!")
print(f"The animal at three is the fourth animal; sweet, it's a {animals[3]}!!")
print(f"The fifth animal is at index 4... Do you speak {animals[4]}?!")
print(f"The animal at 2 is the third animal... Not a {animals[2]} again!?")
print(f"The sixth animal is at index 5. A good example is Perry the {animals[5]}.")
print(f"The animal at index 4 is the fifth animal. Another {animals[4]}!")